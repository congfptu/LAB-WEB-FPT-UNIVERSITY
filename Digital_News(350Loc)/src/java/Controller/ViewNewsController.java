    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void processGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            NewsDAO newsDAO = new NewsDAO();
            News currentNews = null;
            //get id of news and show message error if id invalid , negative or wrong format
            try {
                int id = Integer.parseInt(request.getParameter("id"));
                //get news by id
                currentNews = newsDAO.findById(id);
                //not found articles
                if (currentNews == null) {
                    request.setAttribute("error04", "Not found articles");
                }
            } catch (NumberFormatException e) {
                //set message error if id wrong format
                request.setAttribute("error03", "Error, not found articles");
            }
            request.setAttribute("currentNews", currentNews);
            request.getRequestDispatcher("view/view.jsp").forward(request, response);
        } catch (Exception ex) {
            request.getRequestDispatcher("common/error.jsp").forward(request, response);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void processPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

